    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        if (order.getStatus().equals("Preparing")) {
            JOptionPane.showMessageDialog(null, "Order Already Accepted", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            order.setStatus("Preparing");
            txtStatus.setText("Preparing");
            for (DeliveryMan deliveryMan : ecoSystem.getDeliveryManDirectory().getDeliveryManList()) {
                if (deliveryMan.getName().equals(comboDeliveryMan.getSelectedItem())) {
                    order.setDeliveryMan(deliveryMan);
                    for (UserAccount user : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
                        if (user.getEmployee().getId() == deliveryMan.getEmployeeId()) {
                            user.getWorkQueue().getWorkRequestList().add(order);
                        }
                    }
                    break;
                }
            }

            JOptionPane.showMessageDialog(null, "Order Accepted Successfully", "Successful", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageOrderJPanel workArea = (ManageOrderJPanel) component;
        workArea.loadData();
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        // TODO add your handling code here:
        if (order.getStatus().equals("Cancelled")) {
            JOptionPane.showMessageDialog(null, "Order Already Cancelled", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            order.setStatus("Cancelled");
            txtStatus.setText("Cancelled");
            JOptionPane.showMessageDialog(null, "Order Cancelled Successfully", "Cancelled", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnCancelActionPerformed


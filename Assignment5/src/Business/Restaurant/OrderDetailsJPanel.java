    private void loadData() {

        txtStatus.setText(order.getStatus());
        txtCustName.setText(order.getCustomer().getFullName());
        txtCustomerAddress.setText(order.getCustomer().getAddress());
        SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy hh:mm");
        txtOrderTime.setText(formatter.format(order.getRequestDate()));
        txtCustMsg.setText(order.getMessage());
        txtCustFeedback.setText(order.getCustomerFeedback());

        for (DeliveryMan deliveryMan : ecoSystem.getDeliveryManDirectory().getDeliveryManList()) {
            if (deliveryMan.isAvailable()) {
                comboDeliveryMan.addItem(deliveryMan.getName());
            }
        }

        if (order.getStatus().equals("Delivered")) {
            btnAccept.setEnabled(false);
            btnCancel.setEnabled(false);
        }
        populateTable();

    }

    public void populateTable() {
        double totalprice = 0.0;
        DefaultTableModel model = (DefaultTableModel) tblOrderMenu.getModel();
        model.setRowCount(0);

        for (MenuItem menuItem : order.getMenuItems()) {
            Object[] row = new Object[3];
            row[0] = menuItem;
            row[1] = menuItem.getQuantity();
            row[2] = menuItem.getPrice() * menuItem.getQuantity();
            totalprice += menuItem.getPrice() * menuItem.getQuantity();
            model.addRow(row);
        }

        Object[] row = new Object[2];
        row[0] = "Total";
        row[1] = String.valueOf(totalprice);
        model.addRow(row);

    }


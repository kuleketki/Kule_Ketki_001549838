    private void loadData() {
        //If Update operation is selected
        if (operationId == 1) {
            txtFullName.setText(customer.getFullName());
            txtTelNo.setText(String.valueOf(customer.getPhoneNumber()));
            txtEmail.setText(customer.getEmailAddress());
            txtAddress.setText(customer.getAddress());
            JdateBirthdate.setDate(customer.getDob());
            for (UserAccount userAccount : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
                if (userAccount.getEmployee().getId() == customer.getAccountId()) {
                    txtPassword.setText(userAccount.getPassword());
                    txtUserName.setText(userAccount.getUsername());
                }
            }
            txtUserName.setEditable(false);
            txtPassword.setEditable(false);
        }

    }


    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String fullName = txtFullName.getText();
        String emailId = txtEmail.getText();
        String address = txtAddress.getText();
        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        String telNo = txtTelNo.getText();

        if (txtEmail.getText().isEmpty() || txtFullName.getText().isEmpty() || txtTelNo.getText().isEmpty() || txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty()
                || txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill All Fields ", "Empty Fields", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //validate EMail address
        String emailIdRegex = "^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";
        if (!Pattern.matches(emailIdRegex, emailId)) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Email Address ", "Invalid Email ID", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //validate mobileNo
        String telNumberRegex = "\\d{10}";
        if (!Pattern.matches(telNumberRegex, telNo)) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Telephone No ", "Invalid Telephone No", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (operationId == 0) {
            for (UserAccount account : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
                if (account.getUsername().equals(userName)) {
                    JOptionPane.showMessageDialog(null, "User Name Already Exists", "Invalid Entry", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            for (Customer customer : ecoSystem.getCustomerDirectory().getCustomerList()) {
                if (customer.getEmailAddress() != null && customer.getEmailAddress().equals(emailId)) {
                    JOptionPane.showMessageDialog(null, "Email Id Already Exists ", "Invalid Email ID", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            for (Customer customer : ecoSystem.getCustomerDirectory().getCustomerList()) {
                if (customer.getPhoneNumber() == Long.parseLong(telNo)) {
                    JOptionPane.showMessageDialog(null, "Phone Number already exists", "Invalid Entry", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            Employee employee = ecoSystem.getEmployeeDirectory().createEmployee(userName);
            ecoSystem.getCustomerDirectory().getCustomerList().add(new Customer(employee.getId(), fullName, emailId, Long.parseLong(telNo), address, JdateBirthdate.getDate()));
            UserAccount userAccount = ecoSystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new CustomerRole());
            JOptionPane.showMessageDialog(null, "New Customer Registered", "Entry Added", JOptionPane.INFORMATION_MESSAGE);
        } else {
            //Edit Operation 

            if (customer.getPhoneNumber() != Long.parseLong(telNo)) {
                for (Customer customer : ecoSystem.getCustomerDirectory().getCustomerList()) {
                    if (customer.getPhoneNumber() == Long.parseLong(telNo) && customer.getPhoneNumber() != customer.getPhoneNumber()) {
                        JOptionPane.showMessageDialog(null, "Phone Number already exists", "Invalid Entry", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                }
            }

            if (customer.getEmailAddress().equals(emailId)) {
                for (Customer deliveryMan : ecoSystem.getCustomerDirectory().getCustomerList()) {
                    if (deliveryMan.getEmailAddress() != null && deliveryMan.getEmailAddress().equals(emailId) && !deliveryMan.getEmailAddress().equals(deliveryMan.getEmailAddress())) {
                        JOptionPane.showMessageDialog(null, "Please Enter Valid Email Address ", "Invalid Email ID", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                }
            }

            customer.setFullName(fullName);
            customer.setAddress(address);
            customer.setEmailAddress(emailId);
            customer.setPhoneNumber(Long.parseLong(telNo));
            customer.setDob(JdateBirthdate.getDate());
            JOptionPane.showMessageDialog(null, "Customer Details Updated", "Entry Updated", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnSaveActionPerformed


    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDeliveryMan.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DeliveryMan deliveryMan = (DeliveryMan) tblDeliveryMan.getValueAt(selectedRow, 0);
        AddEditDeliveryManJPanel updateDeliveryMan = new AddEditDeliveryManJPanel(displayPanel, ecoSystem, deliveryMan, 1);
        displayPanel.add("AddRestuarantJPanel", updateDeliveryMan);
        CardLayout layout = (CardLayout) displayPanel.getLayout();
        layout.next(displayPanel);
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDeliveryMan.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DeliveryMan deliveryMan = (DeliveryMan) tblDeliveryMan.getValueAt(selectedRow, 0);
        ecoSystem.getDeliveryManDirectory().getDeliveryManList().remove(deliveryMan);
        UserAccount userAccount = null;
        for (UserAccount ua : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
                if (ua.getEmployee().getId() == deliveryMan.getEmployeeId()) {
                    userAccount = ua;
            }
        }
        ecoSystem.getUserAccountDirectory().getUserAccountList().remove(userAccount);
        loadTableData();
    }//GEN-LAST:event_btnDeleteActionPerformed



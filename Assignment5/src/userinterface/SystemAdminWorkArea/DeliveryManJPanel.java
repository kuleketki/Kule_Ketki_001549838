    public void loadTableData() {
        DefaultTableModel delModel = (DefaultTableModel) tblDeliveryMan.getModel();
        delModel.setRowCount(0);

        for (DeliveryMan deliveryMan : ecoSystem.getDeliveryManDirectory().getDeliveryManList()) {
            Object[] row = new Object[5];
            row[0] = deliveryMan;
            row[1] = deliveryMan.getTelephoneNo();
            row[2] = deliveryMan.getAddress();
            row[3] = deliveryMan.getWorkingHoursInWeek();
            row[4] = deliveryMan.isAvailable();

            delModel.addRow(row);
        }
    }


    private void btnUpdateMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMenuActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        MenuItem menuItem = (MenuItem) tblMenu.getValueAt(selectedRow, 0);
        AddEditMenuItemJPanel workArea = new AddEditMenuItemJPanel(displayPanel, restaurant.getMenu(), menuItem, 1);
        displayPanel.add("AddEditMenuItemJPanel", workArea);
        CardLayout layout = (CardLayout) displayPanel.getLayout();
        layout.next(displayPanel);

    }//GEN-LAST:event_btnUpdateMenuActionPerformed

    private void btnDeleteItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteItemActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        MenuItem menuItem = (MenuItem) tblMenu.getValueAt(selectedRow, 0);
        restaurant.getMenu().getMenuList().remove(menuItem);
        loadMenuTable();
    }//GEN-LAST:event_btnDeleteItemActionPerformed



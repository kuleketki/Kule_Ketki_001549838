    private void btnDriverWorkAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDriverWorkAreaActionPerformed
        // TODO add your handling code here:
        boolean isSerialNoCorrect = false;
        for (Driver driver : uber.getDriverDirectory().getDriverList()) {
            if (driver.getSerialNumber().equalsIgnoreCase(txtSerialNumber.getText())) {
                isSerialNoCorrect = true;
                ManageCarJPanel ms = new ManageCarJPanel(displayPanel, uber, txtSerialNumber.getText());
                displayPanel.add(ms);
                CardLayout layout = (CardLayout) displayPanel.getLayout();
                layout.next(displayPanel);
                break;
            }
        }
        if (!isSerialNoCorrect) {
            JOptionPane.showMessageDialog(null, "Please enter registered car", "Invalid Serial Number", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnDriverWorkAreaActionPerformed


    private void displayPersonData() {
        try {
            txtName.setText(person.getName());
            txtStreet.setText(person.getStreet());
            txtCity.setText(person.getCity());
            txtCountry.setText(person.getCountry());
            txtPincode.setText(person.getPincode());
            txtDob.setText(convertDateToString(person.getBirthDate()));
            txtTelNum.setText(person.getMobileNumber()+"");
            txtFaxNumber.setText(person.getFaxNumber()+"");
            txtEmailAddr.setText(person.getEmailAddr());
            txtSsn.setText(person.getSocialSecurityNum());
            txtMedicalRecord.setText(person.getMedicalRecordNum()+"");
            txtHealthPlanBeneficiary.setText(person.getHealthPlanNum());
            txtBankAccNum.setText(person.getBankAccNum());
            txtCertLicenseNum.setText(person.getCertificateNum());
            txtVehicleIdentifiers.setText(person.getVehicleIdentifiers());
            txtDeviceIdentifiers.setText(person.getDeviceIdentifiers());
            txtLinkedIn.setText(person.getLinkedInId());
            txtIpAddr.setText(person.getIpAddr());
            BufferedImage bioImg = ImageIO.read(new File(person.getBiometricIdentifier()));
            BufferedImage fullFaceImg = ImageIO.read(new File(person.getPhotographicImg()));
            imgBioIdentifiers.setIcon(new ImageIcon(bioImg));
            imgFullPhoto.setIcon(new ImageIcon(fullFaceImg));
            txtUniqueIdentifyingNo.setText(person.getUniqueNum());
        } catch (IOException ex) {
            Logger.getLogger(ViewJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private String convertDateToString(LocalDate date){
        String dateOfMonth = date.getDayOfMonth()+"";
        String month = (date.getMonthValue()+1)+"";
        String year = date.getYear()+"";
        
        String strDate = dateOfMonth+"/"+month+"/"+year ;
        return strDate;
    }

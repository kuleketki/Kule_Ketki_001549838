    public void loadData() {
        for (Patient p : system.getPatientDirectory().getPatientsList()) {
            cbPersonList.addItem(p.getPerson().getFullName());
        }
        txtIdNo.setText(system.getPatientDirectory().getPatientsList().get(0).getPerson().getPersonalIdNo());

    }

    public void refreshTable() {
        int rowCount = encounterTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel) encounterTable.getModel();
        Date d;
        DateFormat f = new SimpleDateFormat("yyyy-MM-dd HH:mm ");

        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (Encounter s : system.getPatientDirectory().getPatientsList().get(cbPersonList.getSelectedIndex()).getEncounterHistory().getEncounters()) {
            d = new Date(s.getTimeStamp().getTime());

            Object row[] = new Object[7];
            row[0] = f.format(d);
            row[1] = s.getVitalSigns().getBodyTemp();
            row[2] = s.getVitalSigns().getBloodPressure();
            row[3] = s.getVitalSigns().getPulseRate();
            row[4] = s.getVitalSigns().getRespirationRate();
            row[5] = s.getVitalSigns().getWeight();
            row[6] = s.getVitalSigns().getHeight();
            model.addRow(row);
        }
    }


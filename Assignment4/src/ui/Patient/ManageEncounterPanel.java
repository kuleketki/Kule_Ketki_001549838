    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = encounterTable.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            system.getPatientDirectory().getPatientsList().get(cbPersonList.getSelectedIndex()).getEncounterHistory().getEncounters().remove(selectedRowIndex);
            refreshTable();
            JOptionPane.showMessageDialog(null, "Record Deleted", "Message", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = encounterTable.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            Patient patient = system.getPatientDirectory().getPatientsList().get(cbPersonList.getSelectedIndex());
            AddEditEncounterPanel addEditEncounterPanel = new AddEditEncounterPanel(displayPanel, system, patient, 1, system.getPatientDirectory().getPatientsList().get(cbPersonList.getSelectedIndex()).getEncounterHistory().getEncounters().get(selectedRowIndex));
            displayPanel.add(addEditEncounterPanel);
            CardLayout layout = (CardLayout) displayPanel.getLayout();
            layout.next(displayPanel);
        }
    }//GEN-LAST:event_btnEditActionPerformed

